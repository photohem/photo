    <p>
카메라를 처음 손에 쥐면 누구나 가족, 아이, 연인, 반려동물 등 소중한 일상의 순간부터 여행지의 아름다운 풍경까지
다양한 장면을 담고 싶어집니다. <br>
그중에서도 많은 사진 애호가들이 한 번쯤 꿈꾸는 것이 바로 밤하늘의 야경, 특히 밝고 둥근 달을 선명하게 찍는 경험입니다. <br>
 <br>
다가오는 정월대보름을 맞아 카메라로 보름달의 아름다움을 사진에 담아보는 건 어떨까요?  <br>
보름달을 보다 멋지고 선명하게 촬영할 수 있는 실전 팁과 촬영 노하우를 소개합니다.  <br>
달은 예쁘지만 촬영이 까다로운 피사체인 만큼, 이번 기회에 제대로 준비해 특별한 달 사진을 남겨보시길 바랍니다
</p>

  <!-- 구분선 -->
  <hr class="guide-divider2" />

<h3>달 사진찍는 법</h3>
<br>
<h4>1. 달 모양, 시각 미리 확인</h4><br>
<img src="./img/skill02-1.jpg" alt="달" style="width:100%; max-width:600px; margin-bottom: 12px;">
<p>달 사진을 잘 찍으려면 먼저 달의 모양(위상)과 뜨는 시각, 위치를 미리 파악하는 것이 중요합니다. 
보름달, 초승달, 상현달 등 원하는 달의 모습에 따라 촬영 시기가 달라지기 때문입니다. <br>
달이 가장 크게 보이고 밝은 보름달은 물론, 초승달이나 그믐달은 주변 풍경과 함께 신비로운 분위기를 
연출할 수 있습니다. 촬영을 계획할 때는 달의 위상과 뜨는 위치, 시간, 고도(하늘에서의 각도)를 확인할 
수 있는 앱(예: PhotoPills, TPE, PlanIt 등)이나 웹사이트를 활용하면 좋습니다. <br>
특히 해가 지기 전후(골든아워, 블루아워)에는 달과 풍경을 함께 담기 좋아 멋진 사진을 얻을 수 있습니다.
</p>
<br><br><br>
<h4>2. 달 사진 찍기 좋은 렌즈</h4><br>
<img src="./img/skill02-2.jpg" alt="달" style="width:100%; max-width:600px; margin-bottom: 12px;">
<p>달을 크게, 선명하게 담으려면 망원 렌즈가 필수입니다. 최소 300mm 이상의 망원 렌즈를 사용하면 
달의 표면과 크레이터까지 디테일하게 촬영할 수 있습니다. <br>
400mm, 500mm, 600mm 등 더 긴 초점거리의 렌즈를 사용할수록 달이 프레임에서 더 크게 보입니다. <br>
만약 표준 줌렌즈(예: 70-200mm)나 크롭 센서 카메라를 사용한다면, 크롭 효과로 인해 달이 더 크게 
찍히는 장점도 있습니다. 
삼각대와 렌즈 손떨림 보정 기능이 있다면 더욱 안정적으로 촬영할 수 있습니다. <br>
고가의 렌즈가 아니어도, 화질이 좋은 망원 렌즈와 크롭 촬영, 후편집(크롭)으로도 충분히 멋진 달 사진을 얻을 수 있습니다.
</p>
<br><br><br>
<h4>3. 달 사진 촬영 세팅 방법</h4><br>
<img src="./img/skill02.jpg" alt="달" style="width:100%; max-width:600px; margin-bottom: 12px;">
<p>달 사진은 수동(Manual) 모드에서 촬영하는 것이 좋습니다. <br>
  추천하는 기본 세팅은 ISO 100~200, 조리개 f/8~f/11, 셔터스피드 1/100~1/250초 정도입니다. <br>
  달은 매우 밝기 때문에 ISO를 낮게, 조리개는 렌즈의 선예도가 좋은 중간값(f/8~f/11)으로, 
  셔터스피드는 달의 움직임에 따라 1/100초 이상으로 맞추면 선명한 결과를 얻을 수 있습니다. <br>
  삼각대를 사용해 흔들림을 줄이고, 라이브뷰에서 최대한 확대해 수동 초점을 맞추면 더욱 
  또렷한 사진을 얻을 수 있습니다. <br>
촬영 후에는 히스토그램을 확인해 노출이 적절한지 점검하고, 필요에 따라 브라케팅 촬영이나 
노출 보정도 활용해보세요.
</p><br>
    
